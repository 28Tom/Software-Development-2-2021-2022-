public class StudentModel {

    private String sName, sNumber;

    public void setName(String s)
    {
        sName = s;
    }

    public void setNumber(String s)
    {
        sNumber = s;
    }


    public String getName()
    {
        return sName;
    }

    public String getNumber()
    {
        return sNumber;
    }

}
